  `RECIPIENT_ID` int(11) NOT NULL COMMENT 'ID del Destinatario',
  `CONTENT` text CHARACTER SET utf8 COLLATE utf8_unicode_ci NOT NULL COMMENT 'Contenido del Mensaje',
  `TIMESTAMP` datetime NOT NULL DEFAULT CURRENT_TIMESTAMP COMMENT 'Fecha y Hora del Mensaje',
  PRIMARY KEY (`MESSAGE_ID`),
  FOREIGN KEY (`SENDER_ID`) REFERENCES `ELEMENTO`(`ELEMENTO_ID`) ON DELETE CASCADE ON UPDATE CASCADE,
  FOREIGN KEY (`RECIPIENT_ID`) REFERENCES `ELEMENTO`(`ELEMENTO_ID`) ON DELETE CASCADE ON UPDATE CASCADE
) ENGINE=InnoDB DEFAULT CHARSET=utf8 COLLATE=utf8_unicode_ci COMMENT='Tabla de Mensajes';

